php.exp = function (arg) {
    // Returns e raised to the power of the number  
    // 
    // version: 1008.1718
    // discuss at: http://phpjs.org/functions/exp
    // +   original by: Onno Marsman
    // *     example 1: \php.exp(0.3);
    // *     returns 1: 1.3498588075760032
    return Math.exp(arg);
};

